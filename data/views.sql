CREATE VIEW DISCOGRAPHY AS
SELECT DISTINCT
    SONGS.SONG_ID,
    SONGS.SONG_NAME,
    SONGS.RELEASE_DATE,
    ARTISTS.ARTIST_ID
FROM SONGS
JOIN SONG_ARTISTS ON SONGS.SONG_ID = SONG_ARTISTS.SONG_ID
JOIN ARTISTS ON SONG_ARTISTS.ARTIST_ID = ARTISTS.ARTIST_ID;

CREATE VIEW ARTISTMOSTPOPULAR AS
SELECT DISTINCT
    SONGS.SONG_ID,
    SONGS.SONG_NAME,
    SONGS.LISTEN_COUNT,
    SONGS.RELEASE_DATE,
    ARTISTS.ARTIST_ID
FROM SONGS
JOIN SONG_ARTISTS ON SONGS.SONG_ID = SONG_ARTISTS.SONG_ID
JOIN ARTISTS ON SONG_ARTISTS.ARTIST_ID = ARTISTS.ARTIST_ID
ORDER BY LISTEN_COUNT DESC;

